    relativeLeft.value[0] = -relativeRight.value[0];
    relativeLeft.value[1] = -relativeRight.value[1];
    relativeLeft.value[2] = -relativeRight.value[2];
    rotation.vectorMultiply(vec3.up, relativeUp).multiply(scale.value[1]);
    relativeDown.value[0] = -relativeUp.value[0];
    relativeDown.value[1] = -relativeUp.value[1];
    relativeDown.value[2] = -relativeUp.value[2];
    rotation.vectorMultiply(vec3.forward, relativeForward).multiply(scale.value[2]);
    vec3.add(translation, relativeForward, end);
    vec3.add(relativeRight, relativeUp, A);
    vec3.add(relativeRight, relativeDown, B);
    vec3.add(relativeLeft, relativeDown, C);
    vec3.add(relativeLeft, relativeUp, D);
    vec3.add(translation, A, startA);
    vec3.add(translation, B, startB);
    vec3.add(translation, C, startC);
    vec3.add(translation, D, startD);
    vec3.add(end, A, endA);
    vec3.add(end, B, endB);
    vec3.add(end, C, endC);
    vec3.add(end, D, endD);
