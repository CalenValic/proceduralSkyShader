import { vec3 } from "./vec3class.js";
import { quaternion } from "./quaternionclass.js";
var relativeRight = new vec3(0, 0, 0);
var relativeLeft = new vec3(0, 0, 0);
var relativeUp = new vec3(0, 0, 0);
var relativeDown = new vec3(0, 0, 0);
var relativeForward = new vec3(0, 0, 0);
var end = new vec3(0, 0, 0);
var A = new vec3(0, 0, 0);
var B = new vec3(0, 0, 0);
var C = new vec3(0, 0, 0);
var D = new vec3(0, 0, 0);
var startA = new vec3(0, 0, 0);
var startB = new vec3(0, 0, 0);
var startC = new vec3(0, 0, 0);
var startD = new vec3(0, 0, 0);
var endA = new vec3(0, 0, 0);
var endB = new vec3(0, 0, 0);
var endC = new vec3(0, 0, 0);
var endD = new vec3(0, 0, 0);
export const OBBtoAABB = (translation, rotation, scale, min, max) => {
    rotation.vectorMultiply(vec3.right, relativeRight).multiply(scale.x);
    relativeLeft.value[0] = -relativeRight.value[0];
    relativeLeft.value[1] = -relativeRight.value[1];
    relativeLeft.value[2] = -relativeRight.value[2];
    rotation.vectorMultiply(vec3.up, relativeUp).multiply(scale.y);
    relativeDown.value[0] = -relativeUp.value[0];
    relativeDown.value[1] = -relativeUp.value[1];
    relativeDown.value[2] = -relativeUp.value[2];
    rotation.vectorMultiply(vec3.forward, relativeForward).multiply(scale.z);
    vec3.add(translation, relativeForward, end);
    vec3.add(relativeRight, relativeUp, A);
    vec3.add(relativeRight, relativeDown, B);
    vec3.add(relativeLeft, relativeDown, C);
    vec3.add(relativeLeft, relativeUp, D);
    vec3.add(translation, A, startA);
    vec3.add(translation, B, startB);
    vec3.add(translation, C, startC);
    vec3.add(translation, D, startD);
    vec3.add(end, A, endA);
    vec3.add(end, B, endB);
    vec3.add(end, C, endC);
    vec3.add(end, D, endD);
    min.x = Math.min(startA.x, startB.x, startC.x, startD.x, endA.x, endB.x, endC.x, endD.x);
    min.y = Math.min(startA.y, startB.y, startC.y, startD.y, endA.y, endB.y, endC.y, endD.y);
    min.z = Math.min(startA.z, startB.z, startC.z, startD.z, endA.z, endB.z, endC.z, endD.z);
    max.x = Math.max(startA.x, startB.x, startC.x, startD.x, endA.x, endB.x, endC.x, endD.x);
    max.y = Math.max(startA.y, startB.y, startC.y, startD.y, endA.y, endB.y, endC.y, endD.y);
    max.z = Math.max(startA.z, startB.z, startC.z, startD.z, endA.z, endB.z, endC.z, endD.z);
};
